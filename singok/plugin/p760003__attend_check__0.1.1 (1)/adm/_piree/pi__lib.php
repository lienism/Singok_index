		# 끝 => 공통_쿼리__조합
		#######################################################



		#######################################################
		# 시작 => 설정__저장하기
		#######################################################

		//=====================================================
		// 프로그램_저장_여부__확인하는__쿼리문
		$sql	 = "SELECT COUNT(*) AS exist FROM `".$piree_table['program']."` WHERE prog_n=".$g_program_n;
		$row	 = sql_fetch($sql);
		$exist = @$row["exist"];


		//=====================================================
		// 시작 => 설정_ROW__유무
		IF ($exist > 0)
		{

			#####################################################
			# 시작 => ROW_있으니__수정하기
			#####################################################

			//===================================================
			// 수정하는__쿼리
			$sql	= "UPDATE `".$piree_table['program']."` SET ";
			$sql .= $sql_save.", ";
