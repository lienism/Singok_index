			# 시작 => ROW_없으니__입력하기
			#####################################################

			//===================================================
			// 입력하는__쿼리
			$sql	= "INSERT INTO `".$piree_table['program']."` SET ";
			$sql .= $sql_save.", ";
			$sql .= "regi_time_n=".G5_SERVER_TIME;


			//===================================================
			// 에러_메세지
			$save_err_msg = "프로그램 설치 정보를 입력하지 못했습니다.";

		}
		// 끝 => 설정_ROW__유무
		//=====================================================


		//=====================================================
		// 시작 => 쿼리_실행
		IF (!sql_query($sql))
		{

			// 에러
			alert ($save_err_msg, $go_url_s);
			EXIT;

		}
