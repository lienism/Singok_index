		#######################################################



		#######################################################
		# 시작 => 공통_쿼리__조합
		#######################################################

		//=====================================================
		// 공통_쿼리__조합
		$sql_save	= "grp_n='".$grp_n."', ";
		$sql_save .= "user_n='".$user_n."', ";
		$sql_save .= "prog_n='".$g_program_n."', ";
		$sql_save .= "prog_c='".$prog_c."', ";
		$sql_save .= "prog_s='".$prog_s."', ";
		$sql_save .= "version_s='".$g_program_version_s."', ";
		$sql_save .= "prog_memo_s='".$prog_memo_s."', ";
		$sql_save .= "is_paid='".$is_paid."', ";
		$sql_save .= "domain_s='".$domain_s."'";

