	function get__prog_info($prog_n)
	{

		//=====================================================
		// 외부변수
		global $piree_table, $is_piree_program_config;


		//=====================================================
		// 넘겨줄__배열_변수
		$prog_info = array();


		//=====================================================
		// 시작 => 피리_프로그램_번호__있으면
		IF ($prog_n > 0)
		{

			//===================================================
			// 피리_프로그램_정보__가져오기
			$sql = "SELECT * FROM `".$piree_table['program']."` WHERE prog_n=".$prog_n;
			$row = sql_fetch($sql, false);


			//===================================================
			// 시작 => 칼럼__없으면
			IF (!isset($row['prog_n']))
			{

				//=================================================
				// 외부_상수_변수
				global $is_admin;


				//=================================================
				// 시작 => 운영자_일반회원__여부
				IF ($is_admin)
				{

					#################################################
					# 시작 => 운영자__이면

					//===============================================
					// 시작 => 설정_화면__아니면
					IF ($is_piree_program_config != 1)
					{

							// 이동할_페이지
							$move_url = G5_ADMIN_URL.'/p'.PIREE_PROG_PLUS_MENU_N.'/';

							// 에러_알림
							alert ("선택하신 프로그램이 설치되지 않았거나 DB정보가 없습니다. piree", $move_url);

					}
					// 끝 => 설정_화면__아니면
					//===============================================

				}
				ELSE
				{

					#################################################
					# 시작 => 일반회원__이면

					// 에러_알림
					alert ("선택하신 프로그램이 설치되지 않았거나 DB정보가 없습니다. piree", G5_URL);

				}
				// 끝 => 운영자_일반회원__여부
				//=================================================

			}
			// 끝 => 칼럼__없으면
			//===================================================


			//===================================================
			// 시작 => 피리_프로그램_번호__맞으면
			IF ($prog_n == $row["prog_n"])
			{

				//=================================================
				// 피리_프로그램_정보__배열
				$prog_info = $row;

			}
			// 끝 => 피리_프로그램_번호__맞으면
			//===================================================

		}
		// 끝 => 피리_프로그램_번호__있으면
		//=====================================================


		//=====================================================
		// 피리_프로그램_정보
		return $prog_info;

	}
