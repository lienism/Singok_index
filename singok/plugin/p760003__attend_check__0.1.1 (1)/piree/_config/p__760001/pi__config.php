	function piree_program_create_table($sql_file_path)
	{

		//=====================================================
		// 시작 => 화일_경로__있으면
		IF ($sql_file_path)
		{

			//===================================================
			// 테이블_생성
			$file = implode('', file($sql_file_path));
			eval("\$file = \"$file\";");


			//===================================================
			// 문자열_치환
			$file = preg_replace('/^--.*$/m', '', $file);
			$file = preg_replace('/`g5_([^`]+`)/', '`'.G5_TABLE_PREFIX.'$1', $file);


			//===================================================
			// 쿼리문__배열_나누기
			$sql_arr = explode(';', $file);


			//===================================================
			// 시작 => 반복문__돌리기
			FOR ($i=0; $i<count($sql_arr); $i++)
			{

				//=================================================
				// 쿼리문_앞뒤__공백_제거
				$sql_in = trim($sql_arr[$i]);


				//=================================================
				// 시작 => 쿼리문__있으면
				IF (trim($sql_in))
				{

					//===============================================
					// 시작 => 테이블_삭제__선택하지_않았으면
					IF ($is_table_drop != 1)
					{

						//=============================================
						// 시작 => 삭제_쿼리__이면
						IF (eregi("^DROP TABLE", $sql_in))
						{

							// 건너뛰기
							continue;

						}
						// 끝 => 삭제_쿼리__이면
						//=============================================

					}
					// 끝 => 테이블_삭제__선택하지_않았으면
					//===============================================


					//===============================================
					// 쿼리__실행하기
					mysql_query($sql_in) or die(mysql_error());

				}
				// 끝 => 쿼리문__있으면
				//=================================================

			}
			// 끝 => 반복문__돌리기
			//===================================================

		}
		// 끝 => 화일_경로__있으면
		//=====================================================

	}
